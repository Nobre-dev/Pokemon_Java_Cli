public class Enemy {

    String EnemyName;
    int EnemyHp;
    int EnemyLevel;
    int EnemyStrength;
    int EnemyDefence;
    String EnemyStatus;

    Enemy(String EnemyName, int EnemyLevel, int EnemyStrength, int EnemyHp, int EnemyDefence, String EnemyStatus){

        this.EnemyName = EnemyName;
        this.EnemyLevel = EnemyLevel;
        this.EnemyStrength = EnemyStrength;
        this.EnemyHp = EnemyHp;
        this.EnemyDefence = EnemyDefence;
        this.EnemyStatus = EnemyStatus;

    }
}
